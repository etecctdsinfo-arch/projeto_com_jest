
function somar(n1, n2){
    let res = n1 + n2
    return res
}

function subtrair(n1, n2){
    let res = n1 - n2
    return res
}

function multiplicar(n1, n2){
    let res = n1 * n2
    return res
}

function dividir(n1, n2){
    let res = n1 / n2
    return res
}

export {somar, subtrair, multiplicar, dividir}